    public void oduncleriGor() throws ParseException
    {
        model.setRowCount(0);
        Odunc_Islemleri o = new Odunc_Islemleri();
        Kitap_Islemleri k = new Kitap_Islemleri();
        ArrayList<ArrayList> oduncler = o.oduncAlinmislariVer(tckn);

        for(ArrayList odunc : oduncler)
        {
            int kitap_id = Integer.valueOf(odunc.get(0).toString());
            String kitap_adi = k.getBookName(kitap_id);
            String verilme_tarihi = o.getVerilmeTarihi(kitap_id, tckn);
            Object[] eklenecek = {kitap_id, kitap_adi, verilme_tarihi};
            model.addRow(eklenecek);
        }
        
    }
    
    public boolean oduncVarMi() throws ParseException
    {
        Odunc_Islemleri o = new Odunc_Islemleri();
        ArrayList<ArrayList> oduncler = o.oduncAlinmislariVer(tckn);
        return oduncler != null;
    }

