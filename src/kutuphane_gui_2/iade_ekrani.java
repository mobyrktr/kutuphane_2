    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Odunc_Islemleri o = new Odunc_Islemleri();
        int secilmis_satir = tbl_odunc.getSelectedRow();
        int kitap_id = 0;
        
        if(secilmis_satir == -1)
        {
            if(tbl_odunc.getRowCount() == 0)
            {
                JOptionPane.showMessageDialog(this, "Ödünç listesi boş.");
            }
            
            else
            {
                JOptionPane.showMessageDialog(this, "Tablodan bir ürün seçmelisiniz.");
            }
        }
        
        else
        {
            kitap_id = Integer.valueOf(model.getValueAt(secilmis_satir, 0).toString());
            String kitap_adi = model.getValueAt(secilmis_satir, 1).toString();
            o.iadeEt(kitap_id, tckn);
            lbl_mesaj.setForeground(Color.green);
            lbl_mesaj.setText(kitap_adi + " iade edildi.");
        }
        
        try {
            oduncleriGor();
        } catch (ParseException ex) {
            Logger.getLogger(iade_ekrani.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

