    public void cezalariGor() throws ParseException, SQLException
    {
        Odunc_Islemleri o = new Odunc_Islemleri();
        Kitap_Islemleri k = new Kitap_Islemleri();
        ArrayList<ArrayList> cezalar = o.tumCezalariVer();
        
        for(ArrayList ceza : cezalar)
        {
            int kitap_id = Integer.valueOf(ceza.get(2).toString());
            String tckn = ceza.get(1).toString();
            String isim = ceza.get(0).toString();
            String kitap_adi = k.getBookName(kitap_id);
            String verilme_tarihi = o.getVerilmeTarihi(kitap_id, tckn);
            int ceza_gun = Integer.valueOf(ceza.get(3).toString());
            String ceza_bedel = String.valueOf(ceza_gun * 0.5) + "TL";
            Object[] eklenecek = {tckn, isim, kitap_adi, verilme_tarihi, ceza_gun, ceza_bedel};
            model.addRow(eklenecek);
        }
    }
    
    public void silinecekleriEkle()
    {
        try 
        {
            model = (DefaultTableModel)tb_sil.getModel();
            model.setRowCount(0);
            Kullanici_Islemleri k = new Kullanici_Islemleri();
            ArrayList<ArrayList> kullanicilar = k.getAllUsers();
            for(ArrayList kullanici: kullanicilar)
            {
                String tckn = kullanici.get(0).toString();
                String ad = kullanici.get(1).toString();
                String soyad = kullanici.get(2).toString();
                Object[] eklenecekler = {tckn, ad, soyad};
                model.addRow(eklenecekler);
            }
        } 
        
        catch (SQLException ex) 
        {
            Logger.getLogger(admin_panel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
