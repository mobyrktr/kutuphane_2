    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int silinecek = tb_sil.getSelectedRow();
        if(model.getRowCount() == 0)
        {
            lbl_sil_mesaj.setForeground(Color.red);
            lbl_sil_mesaj.setText("Tabloda hiç kullanıcı yok.");
        }
        
        else
        {
            if(silinecek == -1)
            {
                lbl_sil_mesaj.setForeground(Color.red);
                lbl_sil_mesaj.setText("Önce tablodan bir seçim yapmalısınız.");
            }
            else
            {
                try {
                    Kullanici_Islemleri k = new Kullanici_Islemleri();
                    Odunc_Islemleri o = new Odunc_Islemleri();
                    String tckn = model.getValueAt(silinecek, 0).toString();
                    if(!o.kitapAlmisMi(tckn))
                    {    
                        k.deleteUsers(tckn);
                        silinecekleriEkle();
                        lbl_sil_mesaj.setForeground(Color.green);
                        lbl_sil_mesaj.setText("Silme işlemi başarılı.");
                    }
                    
                    else
                    {
                        lbl_sil_mesaj.setForeground(Color.red);
                        lbl_sil_mesaj.setText("Kullanıcının kitap(ları) iade etmesi gerekiyor.");
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(admin_panel.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed

